# --------------- PRINTING NUMBERS --------------------

print(3)
# Prints an integer number

print(358)
# Prints another integer

print(5000000)
# Python supports large numbers without any special syntax

print(3 + 5)
# Performs addition first, then prints the result

print(2 * 5)
# Performs multiplication and prints the result

print("I am", 22 , "years old...")
# print() can take multiple values separated by commas
# Python automatically adds spaces between them


